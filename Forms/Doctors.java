    private void UpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateMouseClicked
        int row = Lest.getSelectedRow();
        String id = Lest.getValueAt(row, 0).toString();
        String name = Lest.getValueAt(row, 1).toString();
        String Sp = Lest.getValueAt(row, 2).toString();
        String Salr = Lest.getValueAt(row, 3).toString();

        String stru = "UPDATE DOCTORS SET NAME='" + name + "', Specialization='" + Sp + "', SALARY='" + Salr + "'WHERE NUMBER=" + id;
        boolean ifu = DB.GO.runNunQuery(stru);
        if (ifu) {
            JOptionPane.showMessageDialog(this, "تم تعديل البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
            Listed();
        } else {
            JOptionPane.showMessageDialog(this, "لم يتم تعديل البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdateMouseClicked

