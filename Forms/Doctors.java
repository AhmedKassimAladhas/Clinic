    public void Listed() {
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("رقم المسلسل");
        table.addColumn("الاسم");
        table.addColumn("التخصص");
        table.addColumn("سعر الكشف");

        try {
            Connection con = DB.ConectionDB.setconn();
            String strs = "SELECT * FROM DOCTORS";
            Statement st = con.createStatement();
            ResultSet r = st.executeQuery(strs);

            while (r.next()) {
                table.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),});
                Lest.setModel(table);
            }

        } catch (Exception ex) {

        }
    }
