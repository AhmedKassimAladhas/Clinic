    private void PESearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PESearchKeyTyped
        if ("".equals(PESearch.getText())) {
            PESearch1.setVisible(true);
            PESearch1.setEnabled(true);
        } else {
            PESearch1.setVisible(false);
            PESearch1.setEnabled(false);
        }
        try {
            DefaultTableModel table = new DefaultTableModel();
            table.addColumn("رقم المريض");
            table.addColumn("الاسم   ");
            table.addColumn("التليفون   ");
            table.addColumn("النوع");
            table.addColumn("تاريخ الميلاد   ");
            table.addColumn("العمر");

            String strs = "SELECT * FROM patients WHERE ";
            if (btnname.isSelected()) {
                strs += "Name";
            } else if (btnnum.isSelected()) {
                strs += " Number ";
            } else if (btnphone.isSelected()) {
                strs += " Phone ";
            } else {
                strs = "SELECT * FROM patients";
            }
            strs += " LIKE '%" + PESearch.getText() + "%'";

            Connection con = DB.ConectionDB.setconn();
            Statement st = con.createStatement();
            ResultSet r = st.executeQuery(strs);

            while (r.next()) {
                table.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),});
                LPE.setModel(table);
            }
            LPE.setModel(table);
        } catch (Exception ex) {
            ex.getMessage();
        }
    }//GEN-LAST:event_PESearchKeyTyped

