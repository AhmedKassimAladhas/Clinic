    public void Lestresv() {
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("رقم الحجز");
        table.addColumn("تاريخ الحجز");
        table.addColumn("اسم المريض");
        table.addColumn("اسم الدكتور");
        table.addColumn("الكشف");

        try {
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            String dater = f.format(Rdate.getDate());
            Connection con = DB.ConectionDB.setconn();
            Statement st = con.createStatement();
            String strs = "SELECT * FROM reservations_data WHERE DOCNAM LIKE '%" + donam.getSelectedItem() + "%' AND DATE_RESERVAT LIKE '%" + dater + "%'";
            ResultSet r = st.executeQuery(strs);

            while (r.next()) {
                table.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(4),
                    r.getString(5),
                    r.getString(9),});
                LRE.setModel(table);
            }
            LRE.setModel(table);
        } catch (Exception ex) {

        }
    }
