    private void AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddMouseClicked
        if (DName.getSelectedItem()== null || "".equals(Days.getSelectedItem()) || "".equals(from.getValue()) || "".equals(to.getValue()) || "".equals(Shift.getSelectedItem())) {
            JOptionPane.showMessageDialog(this, "رجاء ادخال جميع البيانات ", "خطأ", JOptionPane.ERROR_MESSAGE);
        } else {
            SimpleDateFormat fr = new SimpleDateFormat("HH:mm");
            String Sfrom = fr.format(from.getValue());
            String Sto = fr.format(to.getValue());
            String stri = "INSERT INTO doc_times VALUES (" + docid.getText() + ", '" + Days.getSelectedItem() + "', '" + Sfrom + "', '" + Sto + "', "+Shift.getSelectedItem()+")";
            boolean ifadd = DB.GO.runNunQuery(stri);
            if (ifadd) {
                JOptionPane.showMessageDialog(this, "تم تسجيل البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
                //clearData();
                Days.setSelectedItem("");
                Shift.setSelectedItem("");
                
                DName.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "لم يتم تسجيل البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_AddMouseClicked

