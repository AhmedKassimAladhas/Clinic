public void Listed() {
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("رقم التسلسل");
        table.addColumn("اليوم");
        table.addColumn("من");
        table.addColumn("إلى");
        table.addColumn("رقم الشيفت");

        try {
            Connection con = DB.ConectionDB.setconn();
            String strs = "SELECT * FROM doc_times WHERE dnum like '%" + DID.getText() + "%'";
            Statement st = con.createStatement();
            ResultSet r = st.executeQuery(strs);

            while (r.next()) {
                table.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                });
                Lest.setModel(table);
                if (r.getString(1) == null) {
                    table.setNumRows(0);
                }
            }
        } catch (Exception ex) {

        }
    }
