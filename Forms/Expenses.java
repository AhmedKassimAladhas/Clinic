    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1) {
            int row = lestexp.getSelectedRow();
            String nu = lestexp.getValueAt(row, 0).toString();
            int c = JOptionPane.showConfirmDialog(this, "اضغط موافق لتأكيد الحذف", "تنبيه", JOptionPane.OK_CANCEL_OPTION);
            if (c == JOptionPane.OK_OPTION) {
                String strd = "DELETE FROM EXPENSES WHERE ANUM=" + nu;
                boolean ifd = DB.GO.runNunQuery(strd);
                if (ifd) {
                    JOptionPane.showMessageDialog(this, "تم حذف البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
                    lestexp();
                    id.setText(DB.GO.autoNumber("expenses", "anum"));
                } else {
                    JOptionPane.showMessageDialog(this, "لم يتم حذف البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "لم يتم حذف البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_DeleteMouseClicked

