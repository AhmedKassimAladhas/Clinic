    private void AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1) {
            try {
                if ("".equals(id.getText()) || "".equals(dateday.getText()) || namereg.getSelectedItem() == null || "".equals(costexp.getText()) || "".equals(expens.getText()) || "".equals(nam.getText())) {
                    JOptionPane.showMessageDialog(this, "رجاء ادخال جميع البيانات ");
                } else {
                    int c = JOptionPane.showConfirmDialog(this, "لتأكيد حفظ البيانات اضغط موافق", "تنبيه", JOptionPane.OK_CANCEL_OPTION);
                    if (c == JOptionPane.OK_OPTION) {
                        String stra = "INSERT INTO EXPENSES VALUES(" + id.getText() + ", '" + expens.getText() + "', '" + namereg.getSelectedItem() + "', '"
                                + nam.getText() + "', '" + dateexp.getText() + "', '" + costexp.getText() + "', '" + "0" + "')";
                        boolean ifadd = DB.GO.runNunQuery(stra);
                        if (ifadd) {
                            JOptionPane.showMessageDialog(this, "تم حفظ البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
                            costexp.setText("");
                            expens.setText("");
                            namereg.setSelectedItem(null);
                            id.setText(DB.GO.autoNumber("expenses", "anum"));
                            expens.requestFocus();
                        } else {
                            JOptionPane.showMessageDialog(this, "لم يتم حفظ البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
                            expens.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "لم يتم حفظ البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
                        expens.requestFocus();
                    }
                }
            } catch (Exception ex) {
                ex.getMessage();
            }
        }
    }//GEN-LAST:event_AddMouseClicked

