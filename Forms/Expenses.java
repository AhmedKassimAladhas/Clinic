    public void lestexp() {
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("رقم الصرف");
        table.addColumn("المصروفات");
        table.addColumn("صرف من");
        table.addColumn("سجل بواسطة");
        table.addColumn("تاريخ الصرف");
        table.addColumn("السعر");
        table.addColumn("تم دفع");
        try {
            Connection con = DB.ConectionDB.setconn();
            Statement st = con.createStatement();
            String strs = "SELECT * FROM EXPENSES WHERE Date_of_exp ='" + dateday.getText() + "'";
            ResultSet r = st.executeQuery(strs);

            while (r.next()) {
                table.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),
                    r.getString(7),});
                lestexp.setModel(table);
            }
            lestexp.setModel(table);
        } catch (Exception ex) {
            ex.getMessage();
        }
    }
