public void lested(){
    DefaultTableModel table = new DefaultTableModel();
    table.addColumn("رقم التسلسل");
    table.addColumn("الاسم");
    table.addColumn("النسبة");
    table.addColumn("الموبايل");
    table.addColumn("تاريخ التعيين");
    table.addColumn("تاريخ الميلاد");
    
    try{
        Connection con = DB.ConectionDB.setconn();
        String strs = "SELECT * FROM EMPLOYEES";
        Statement st = con.createStatement();
        ResultSet r = st.executeQuery(strs);
        
        while(r.next()){
            table.addRow(new Object[]{
                r.getString(1),
                r.getString(2),
                r.getString(3),
                r.getString(4),
                r.getString(5),
                r.getString(6),
            });
            LestEmp.setModel(table);
        }
    }catch(Exception ex){}
}
    
