    private void AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddMouseClicked
        if("".equals(ID.getText()) || "".equals(Name.getText()) || "".equals(hiring.getCalendar()) || "".equals(birth.getCalendar()) || "".equals(Phone.getText()) || "".equals(Salary.getText())){
            JOptionPane.showMessageDialog(this, "رجاء ادخال جميع البيانات ", "خطأ", JOptionPane.ERROR_MESSAGE);
        }else{
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            String hDate = f.format(hiring.getDate());
            String bDate = f.format(birth.getDate());
            String str = "INSERT INTO employees VALUES ("+ID.getText()+", '"+Name.getText()+"', '"+Salary.getText()+"', '"+Phone.getText()+"', '"+hDate+"', '"+bDate+"')";
            boolean ifadd = DB.GO.runNunQuery(str);
            if(ifadd){
                JOptionPane.showMessageDialog(this, "تم تسجيل البيانات", "نجح", JOptionPane.WARNING_MESSAGE);
                
            }else{
                JOptionPane.showMessageDialog(this, "لم يتم تسجيل البيانات", "خطأ", JOptionPane.ERROR_MESSAGE);
            }
        }
        clearData();
    }//GEN-LAST:event_AddMouseClicked

