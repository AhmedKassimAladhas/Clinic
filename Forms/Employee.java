        //SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        String Hiring = LestEmp.getValueAt(row, 4).toString();
        String Birth = LestEmp.getValueAt(row, 5).toString();
        
        String strU = "UPDATE EMPLOYEES SET NAME='"+Name+"', SALARY='"+Salary+"', PHONE='"+Phone+"', HIRING_DATE='"+Hiring+"', BIRTH_DATE='"+Birth+"' WHERE NUMBER="+ID;
